package Liam.season1;

public class Assigment_ops {
    public static void main(String[] args) {

        // Default Assignment
        int pix = 10;
        System.out.println("pix: " + pix);

        // Addition
        pix += 10;
        System.out.println("pix: " + pix);

        // Addition of 1
        pix++;
        System.out.println("pix: " + pix);

        // Subtraction
        pix -= 10;
        System.out.println("pix: " + pix);

        // Subtraction of 1
        pix--;
        System.out.println("pix: " + pix);

        // Multiplication
        pix *= 2;
        System.out.println("pix: " + pix);


    }
}
